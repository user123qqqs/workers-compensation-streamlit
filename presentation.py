import streamlit as st
import reveal_slides as rs

def presentation_page():
    st.title("Презентация проекта")

    presentation_markdown = r'''
# Прогнозирование стоимости страховых выплат
---
## Введение
- Задача: предсказать целевую переменную стоимости страхового случая
- Данные: Workers Compensation (OpenML ID: 42876)
- Решение: Streamlit-приложение с многостраничной навигацией
---
## Бизнес-смысл
- Оценка будущих выплат по новому случаю
- Планирование резервов и управление риском
- Выявление факторов, влияющих на стоимость
---
## Данные и признаки
- Числовые и категориальные признаки (возраст, занятость, зарплата и т.д.)
- Признаки из дат:
  - год, месяц, день, час, день недели для даты инцидента и даты репорта
---
## Предобработка
- Пропуски:
  - числовые → заполнение медианой
  - категориальные → заполнение модой/значением "unknown"
- Кодирование категориальных:
  - LabelEncoder (перевод категорий в числовой вид)
- Масштабирование числовых:
  - StandardScaler
---
## Модели
- Baseline: Линейная регрессия / Ridge
- Основная модель: RandomForestRegressor
- Почему RandomForest:
  - учитывает нелинейности и взаимодействия признаков
  - устойчивее на сложных данных по сравнению с линейной моделью
---
## Оценка качества
- Разбиение train/test (настраиваемая доля теста)
- Метрики:
  - MAE, MSE, RMSE, R²
- Визуализации:
  - "прогноз vs факт"
  - распределение остатков
---
## Интерпретация
- Permutation importance:
  - оценка влияния признаков на качество модели
  - построение таблицы и графика топ-признаков
---
## Интерфейс приложения
- Просмотр данных (первые строки)
- Выбор целевой переменной
- Обучение модели (кнопка)
- Отображение метрик и графиков
- Форма предсказания для одной записи
---
## Возможные улучшения
- One-Hot Encoding для категорий (вместо LabelEncoder)
- Лог-преобразование цели (log1p) для тяжёлого хвоста
- Кросс-валидация и подбор гиперпараметров
- Дополнительные признаки (регион, профессия, отрасль)
---
## Заключение
- Построен end-to-end пайплайн: данные → модель → оценка → прогноз
- Реализовано интерактивное приложение для демонстрации результатов
'''

    with st.sidebar:
        st.header("Настройки презентации")
        theme = st.selectbox("Тема", ["black", "white", "league", "beige", "sky", "night"], index=0)
        height = st.number_input("Высота слайдов", value=540)
        transition = st.selectbox("Переход", ["slide", "convex", "concave", "zoom"], index=0)
        plugins = st.multiselect("Плагины", ["highlight", "notes", "search", "zoom"], ["highlight"])

    rs.slides(
        presentation_markdown,
        height=height,
        theme=theme,
        config={"transition": transition, "plugins": plugins},
        markdown_props={"data-separator-vertical": "^--$"},
    )

presentation_page()
